void LCD_GPIO_Initialisation()
{
	GPIO_ResetBits(GPIOA, GPIO_Pin_0);
	GPIO_ResetBits(GPIOA, GPIO_Pin_1 | GPIO_Pin_5);
	GPIO_ResetBits(GPIOA, GPIO_Pin_2);
	GPIO_SetBits(GPIOA, GPIO_Pin_3);
	GPIO_SetBits(GPIOA, GPIO_Pin_4);
	Delay(0x1479E);
	GPIO_SetBits(GPIOA, GPIO_Pin_0);
}

void LCD_CMD_Initialisation(unsigned int number)
{
	unsigned int i=0;
	for(i=0;i<=number;i++)
	{
		LCD_Write_Byte(0x21, LCD_COMMAND, i);
		LCD_Write_Byte(0xB7, LCD_COMMAND, i);
		LCD_Write_Byte(0x14, LCD_COMMAND, i);
		LCD_Write_Byte(0x06, LCD_COMMAND, i);
		LCD_Write_Byte(0x20, LCD_COMMAND, i);
		LCD_Clear(i);
		LCD_Write_Byte(0x0c, LCD_COMMAND, i);
	}
}

