void LCD_Write_Byte(unsigned char data, unsigned char mode, unsigned char type)
{
	unsigned char i;
	if(type == 0)
	{
		LCD_CS_1(1);
		LCD_CS_0(0);
	}

	else if(type == 1)
	{
		LCD_CS_0(1);
		LCD_CS_1(0);
	}

	if (mode == 0)
	{
			LCD_DC(0);
	}

	else if(mode == 1)
	{
			LCD_DC(1);
	}

	for (i = 0; i < 8; i++)
	{
		LCD_DIN(data & 0x80); //SPI_MO = dat & 0x80;
		data = data << 1;
		LCD_SCK(0); //SPI_SCK = 0;
		LCD_SCK(1); //SPI_SCK = 1;
	}

	LCD_CS_0(1);
	LCD_CS_1(1);
}

void LCD_Write_Char(unsigned char c, unsigned char type)
{
	unsigned char line;
	unsigned char ch = 0;

	c = c - 32;

	for (line = 0; line < 6; line++)
	{
		ch = font6_8[c][line];
		if(type == 0)
		{
			LCD_Write_Byte(ch, LCD_DATA, 0);
		}

		else if(type == 1)
		{
			LCD_Write_Byte(ch, LCD_DATA, 1);
		}
	}
}

void LCD_Write_String(char *s, unsigned char type)
{
	unsigned char ch;
	while (*s != '\0')
	{
		ch = *s;
		if(type == 0)
		{
			LCD_Write_Char(ch, 0);
		}
		else if( type == 1)
		{
			LCD_Write_Char(ch, 1);
		}
		s++;
	}
}

